<?php
    namespace CodeSteppers\Generated\Subscriber\Delete;
    
    use Exception;
    use CodeSteppers\Generated\Subscriber\Error\Error;
    use CodeSteppers\Generated\Subscriber\Error\OperationError;
    
    class DeleteController
    {
        private $operationError;
    
        private $deleter;
    
        public function __construct(OperationError $operationError, Deleter $deleter)
        {
            $this->operationError = $operationError;
            $this->deleter = $deleter;
        }
    
        public function delete(string $id)
        {
            try {
                return $this->deleter->delete($id);
            } catch (Exception $err) {
                $this->operationError->addField(Error::getOperationError());
                throw $this->operationError;
            }
        }
    }

  